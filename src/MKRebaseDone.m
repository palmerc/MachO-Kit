+ (NSString*)name
{ return @"REBASE_OPCODE_DONE"; }

//|++++++++++++++++++++++++++++++++++++|//
+ (uint32_t)canInstantiateWithOpcode:(uint8_t)opcode
{
    if (self != MKRebaseDone.class)
        return 0;
    
    return opcode == [self opcode] ? 10 : 0;
}

