@implementation MKRebaseSetTypeImmediate

//|++++++++++++++++++++++++++++++++++++|//
+ (uint8_t)opcode
{ return REBASE_OPCODE_SET_TYPE_IMM; }

//|++++++++++++++++++++++++++++++++++++|//
+ (NSString*)name
{ return @"REBASE_OPCODE_SET_TYPE_IMM"; }

//|++++++++++++++++++++++++++++++++++++|//
+ (uint32_t)canInstantiateWithOpcode:(uint8_t)opcode
{
    if (self != MKRebaseSetTypeImmediate.class)
        return 0;
    
    return opcode == [self opcode] ? 10 : 0;
}

